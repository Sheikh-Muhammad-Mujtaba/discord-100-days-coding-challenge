let age: number = 22; // Declare a variable 'age' and assign the value 22 to it.

// Check if age is greater than 20
if (age > 20) {
  console.log("pass"); // If age is greater than 20, log "pass" to the console.
} 
// Check if age is less than 20
else if (age < 20) {
  console.log("return"); // If age is less than 20, log "return" to the console.
} 
// If age is neither greater than 20 nor less than 20, it must be equal to 20
else {
  console.log("reboot"); // If age is equal to 20, log "reboot" to the console.
}
