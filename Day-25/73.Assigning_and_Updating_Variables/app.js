// This function, 'updateVariable', demonstrates how to assign and update variable values.
function updateVariable() {
    // The variable 'number' is initialized with the value 10.
    var number = 10;
    // The initial value of 'number' is logged to the console.
    console.log("Initial value:", number);
    // The value of 'number' is then updated to 20.
    number = 20;
    // The updated value of 'number' is logged to the console.
    console.log("Updated value:", number);
}
// We call 'updateVariable' to execute the function and demonstrate the concept of value assignment and updating.
updateVariable();
