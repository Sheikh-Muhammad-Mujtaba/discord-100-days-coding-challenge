// This sets up an object for a bike with specific details
let bike = {
    make: "Honda",                // property 'make' with a value of 'Honda'
    model: "CG-125",              // property 'model' with a value of 'CG-125'
    year: 2022                   // property 'year' with a value of 2022
};

// Accessing and showing the bike's model
console.log(bike.model); // Outputs: CG-125
// We use dot notation (bike.model) to get the value of the 'model' property from our object