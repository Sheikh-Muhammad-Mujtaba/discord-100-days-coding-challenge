// This sets up an object for a car with specific details
let car = {
    make: "Toyota",                // property 'make' with a value of 'Toyota'
    model: "Corolla",              // property 'model' with a value of 'Corolla'
    year: 2020               // property 'year' with a value of 2020
};

// Adding a new property 'color' and updating 'year'
car.color = "blue"; // Adds a new property 'color' with a value of 'blue'
car.year = 2021; // Updates the 'year' property with a value of 2021

// Showing the updated car object
console.log(car); // Outputs the car object with the new color and updated year
// Now our car is blue and its model year is updated to 2021.