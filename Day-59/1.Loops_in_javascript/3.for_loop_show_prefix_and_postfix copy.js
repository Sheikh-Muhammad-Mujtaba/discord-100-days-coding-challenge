// Which values get shown by the "for" loop?

// The prefix form
for (let i = 0; i < 5; i++) // Loop from i=0 to i=4 with incrementing i by 1 in each iteration
  alert(i); // Display the current value of i

//it will show  the value 0 to 4



// The postfix form
for (let i = 0; i < 5; ++i) // Loop from i=0 to i<5 with incrementing i by 1 before each iteration
  alert(i); // Display the current value of i

//it will show  the value 0 to 4
// yes both forms show the same value
