// Which values does the while loop show?

// The prefix form
let i1 = 0; // Initialize i with the value 0

while (++i1 < 5) // Loop while incrementing i before each comparison with 5
  alert(i1); // Display the current value of i if it is less than 5
//it will show  the value 1 to 4



// The postfix form
let i2 = 0; // Initialize i with the value 0

while (i2++ < 5) // Loop while comparing i with 5 and then incrementing i
  alert(i2); // Display the current value of i after the increment
//it will show  the value 1 to 5
