// Calculates the absolute difference between -5 and 5
// The Math.abs() function returns the absolute value of a number, ignoring its sign
const difference: number = Math.abs(-5 - 5);

// Outputs: 10
console.log(difference); // This line shows the absolute value of the difference between -5 and 5, ignoring the sign.

