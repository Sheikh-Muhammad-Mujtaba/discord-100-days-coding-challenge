// Define a variable called 'myIdealDay' that will hold a multiline string
var myIdealDay;
// Using template literals to define the multiline string
// Template literals are enclosed by back-ticks (`), not single or double quotes
// They allow for string interpolation, which means you can include variables or expressions inside the string
myIdealDay = "My ideal day would involve:\n1. Waking up early and going for a jog.\n2. Spending a few hours coding on a personal project.\n3. Ending the day by reading a good book.";
// Log the multiline string to the console
console.log(myIdealDay);
