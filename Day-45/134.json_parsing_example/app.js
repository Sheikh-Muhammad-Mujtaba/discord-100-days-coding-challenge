// Defines a JSON string
var jsonString = '{"name":"Alice","age":30,"city":"Wonderland"}';
// Parses the JSON string back into a JavaScript object using JSON.parse()
var person = JSON.parse(jsonString);
// Logs the parsed JavaScript object to the console
console.log(person); // Outputs the original object
// This snippet demonstrates how to take a JSON string and convert it back into a JavaScript object.
