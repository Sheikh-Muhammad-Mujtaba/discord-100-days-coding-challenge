// Defines a JavaScript object named 'person' with properties 'name', 'age', and 'city'
var person = {
    name: "Alice",
    age: 30,
    city: "Wonderland",
};
// Converts the 'person' object into a JSON string using JSON.stringify()
var jsonString = JSON.stringify(person);
// Logs the resulting JSON string to the console
console.log(jsonString); // Outputs: {"name":"Alicea","age":30,"city":"Wonderland"}
