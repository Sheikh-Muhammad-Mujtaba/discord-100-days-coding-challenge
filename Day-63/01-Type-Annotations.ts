// Declaring and initializing variables with explicit types
let myNumberExOne: number = 42; // Declaring a number variable and assigning it a value of 42
let myStringExOne: string = "Hello, TypeScript"; // Declaring a string variable and assigning it a string value

// Outputting variables to the console
console.log("My Number Is:", myNumberExOne); // Logging the value of myNumberExOne to the console
console.log("My String Is:", myStringExOne); // Logging the value of myStringExOne to the console

// Another way to declare and initialize variables, separating declaration and assignment
let myNumberExTwo: number; // Declaring a number variable without assigning an initial value
let myStringExTwo: string; // Declaring a string variable without assigning an initial value

myNumberExTwo = 42; // Assigning a value of 42 to myNumberExTwo
myStringExTwo = "Hello, TypeScript"; // Assigning a string value to myStringExTwo

// Outputting variables to the console
console.log("My Number Is:", myNumberExTwo); // Logging the value of myNumberExTwo to the console
console.log("My String Is:", myStringExTwo); // Logging the value of myStringExTwo to the console
